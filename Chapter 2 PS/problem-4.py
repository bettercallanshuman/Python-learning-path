# PS 4: Use comparison operators to find out whether a given variable 'a' 
# is greater than 'b' or not. Take a = 34 and b = 80.

a = 34
b = 80

# The comparison operator (>) evaluates the expression and returns a Boolean
is_greater = a > b

# Using an f-string to make the output clear
print(f"Is {a} greater than {b}? {is_greater}")