# Lists are sort of containers, which holds the set of values of any datatypes.

friends = ['Akash', 109, 0.098, 'True', 'False', 'blablabla'] # The values in the square brackets are known as ELMENTS in LIST
print(friends[5])

#  Unlike strings, lists are mutable i.e. we can change the position, replace, even delete the existing elements in the list if we want.
#  Example :

friends[5] = "Alina"
print(friends[5])

