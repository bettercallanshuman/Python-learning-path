a = "Anshuman" # String data type is used to represent text. It is a sequence of characters enclosed in quotes.
b = 'Anshuman' # We can use either single quotes or double quotes to represent a string in python. Both are valid and will produce the same result.
c = """Anshuman""" # We can also use triple quotes to represent a string in python. This is useful when we want to include both single and double quotes in the string without escaping them.
print(a)
print(b)
print(c)